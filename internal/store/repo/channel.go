// ResolveRoute looks up the agentID / sessionID mapped by "channel+peer".
// Returns empty strings and found=false when no route matches (caller decides fallback logic).
func (r *ChannelRepo) ResolveRoute(ctx context.Context, channelID, peer string) (agentID, sessionID string, found bool, err error) {
	row := r.db.QueryRowContext(ctx,
		`SELECT COALESCE(agent_id,''),COALESCE(session_id,'')
		 FROM channel_routes WHERE channel_id=? AND peer=?`,
		channelID, peer)
	err = row.Scan(&agentID, &sessionID)
	if err == sql.ErrNoRows {
		return "", "", false, nil
	}
	if err != nil {
		return "", "", false, err
	}
	return agentID, sessionID, true, nil
}

// UpsertRoute creates or updates the "channel+peer → session" route binding.
func (r *ChannelRepo) UpsertRoute(ctx context.Context, id, channelID, peer, agentID, sessionID string) error {
	_, err := r.db.ExecContext(ctx,
		`INSERT INTO channel_routes(id,channel_id,peer,agent_id,session_id)
		 VALUES(?,?,?,?,?)
		 ON CONFLICT(channel_id,peer) DO UPDATE SET session_id=excluded.session_id`,
		id, channelID, peer, agentID, sessionID)
	return err
}
